<?php
	
	/*		//	OPERADORES DE COMPARAÇÃO	//		*/

	$a = 30;
	$b = 55;
	$c = 55.0;

	var_dump($a > $b); 	//	maior que

	echo "<br>";

	var_dump($a < $b); 	//	menor que

	echo "<br>";

	var_dump($a == $b); 	//	igual a

	echo "<br>";

	var_dump($a === $c); 	//	igual a (mesmo tipo)

	echo "<br>";

	var_dump($a != $b); 	//	diferente

	echo "<br>";

	var_dump($a !== $b); 	//	diferente (e tipo de dado)



?>